package Problem2;

import java.util.Objects;

public class Name {
    String name;
    final String firstForm;

    public Name(String name){
        this.name = name;
        firstForm = name;
    }

    public String signAsHub(){
        return  name + "*";
    }

    public String toString(){
        return name;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Name other = (Name) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }
    
}
